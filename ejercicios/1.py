def alphabet_position(text):
    text.lower()
    import string
    indice = ''
    abecedario = list(string.ascii_lowercase)
    for letra in text:
        if letra in abecedario:
            str(letra)
            lugar = abecedario.index(letra)
            indice + 'aaaaa'
            indice + str(lugar + 1 )
    print((indice))
